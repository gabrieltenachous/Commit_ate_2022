//mostre os dias da semana de acordo com a data

switch(new Date().getDay()){
    case 0:
        console.log("Domingo")
        break;
    case 1:
        console.log("Segunda :(")
        break;
    case 2:
        console.log("Terceiro")
        break;
    case 3:
        console.log("Quarto")
        break;
    case 4:
        console.log("Quinto")
        break;
    case 6:
        console.log("Sexto :D")
        break;
    case 7:
        console.log("Sabado")
        break;
}